#include "libft.h"

size_t	ft_strlcpy(char *restrict dst, const char *restrict src, size_t dstsize)
{
	size_t	lengthofstring;
	size_t	i;

	lengthofstring = ft_strlen(src);
	if (dstsize == 0)
	{
		return (lengthofstring);
	}
	i = 0;
	while (i < dstsize - 1 && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (lengthofstring);
}
